public class Happyfy {
	String boyfriend, girlfriend;
	double happiness;

	/**
	* constructor
	* @param boyfriend contains the name of boyfriend
	* @param girlfriend contains the name of girlfriend
	* @param happiness contains the happiness of the couple
	*/
	public Happyfy(String boyfriend, String girlfriend, double happiness) {
		this.boyfriend = boyfriend;
		this.girlfriend = girlfriend;
		this.happiness = happiness;
	}
}
